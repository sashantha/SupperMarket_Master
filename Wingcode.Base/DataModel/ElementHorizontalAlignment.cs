namespace Wingcode.Base.DataModel
{
    /// <summary>
    /// An enum for horizontal value for an element
    /// </summary>
    public enum ElementHorizontalAlignment
    { 
        /// <summary>
        /// Aligned to left
        /// </summary>
        Left = 0,

        /// <summary>
        /// Aligned to center
        /// </summary>
        Center = 1,

        /// <summary>
        /// Aligned to right
        /// </summary>
        Right = 2,

        /// <summary>
        /// Fills the width
        /// </summary>
        Stretch = 3
    }
}
